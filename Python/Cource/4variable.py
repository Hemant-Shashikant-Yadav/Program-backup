# Many Values to Multiple Variables

x, y, z = "Orange", "Banana", "Cherry"
print("x = "+x)
print("y = "+y)
print("z = "+z)


# One Value to Multiple Variables
x = y = z = "Orange"
print("x = "+x)
print("y = "+y)
print("z = "+z)

# Unpack a Collection

fruits = ["apple", "banana", "cherry"]
x, y, z = fruits
print("x = "+x)
print("y = "+y)
print("z = "+z)


print(x, y, z)
print(x + y + z)

