public class arithmetic_operators {
    public static void main(String[] args) {
        int a = 100;
        int b = 20;

        System.out.println("A = " + a);
        System.out.println("B = " + b);

        int sum = a + b;
        System.out.println("Addition operation = " + sum);

        int sub = a - b;
        System.out.println("Subtraction operation is = " + sub);

        int mul = a * b;
        System.out.println("Multiplication operation is = " + mul);

        float div = a / b;
        System.out.println("Multiplication operation is = " + div);

        float mod = a % b;
        System.out.println("Modulas operation is = " + mod);
    }
}
